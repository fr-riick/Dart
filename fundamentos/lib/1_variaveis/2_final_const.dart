
void main(){

  // Modificador final e const serve para que a variável não possa ser alterada depois de receber um valor.

  // Final
  // tempo de execução
  // o que significa que você pode atribuir um valor a uma variável final durante a execução do programa.
  final nameFinal = "Gustavo enrique";
  final String test = nameFinal;
  print(test);
  
  // Const
  // tempo de compilação
  // Isso significa que você deve atribuir um valor constante a uma variável const no momento em que a declara.
  const nameConst = 'Gustavo Enrique';
  print(nameConst);

  
}